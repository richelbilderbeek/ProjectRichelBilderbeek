std::ostream& ribi::operator<<(std::ostream& os, const DotMatrixChar& m)
{
  const int height = m.GetMatrixHeight();
  const int width   = m.GetMatrixWidth();
  for (int y=0; y!=height; ++y)
  {
    for (int x=0; x!=width; ++x)
    {
      os << m.GetMatrix(x,y);
    }
    if (y + 1 != height) os << '\n';
  }
  return os;
}
