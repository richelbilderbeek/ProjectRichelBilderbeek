    case '\\': //Own addition
      v[0][0] = 0; v[0][1] = 0; v[0][2] = 0; v[0][3] = 0; v[0][4] = 0;
      v[1][0] = 1; v[1][1] = 0; v[1][2] = 0; v[1][3] = 0; v[1][4] = 0;
