//---------------------------------------------------------------------------
void DotMatrix(
  Extctrls::TImage * const image,
  const int x_co,
  const int y_co,
  const char c)
{
  const boost::array<boost::array<int,5> ,7> v(GetMatrix(c));

  for (int y=0; y!=7; ++y)
  {
    for (int x=0; x!=5; ++x)
    {
      const int x1 = x_co + x;
      const int y1 = y_co + y;
      if (v[y][x] == 1)
        SetPixel(image,x1,y1,  0,  0,  0);
      else
        SetPixel(image,x1,y1,255,255,255);
    }
  }

}
//---------------------------------------------------------------------------
void DotMatrix(
  Extctrls::TImage * const image,
  const int x_co,
  const int y_co,
  const std::string& s)
{
  const int sz = s.size();
  for (int x=0; x!=sz; ++x)
  {
    DotMatrix(image,x_co + (x*6),y_co,s[x]);
  }

}
//---------------------------------------------------------------------------
void DotMatrix(
  Extctrls::TImage * const image,
  const int x_co,
  const int y_co,
  const std::vector<std::string>& v)
{
  const int n_rows = v.size();
  for (int y=0; y!=n_rows; ++y)
  {
    DotMatrix(image,x_co,y_co+(y*8),v[y]);
  }

}
